function squareNumbers(){
	let number1 = 1;
	let number2 = 2;
	let number3 = 3;
	let number4 = 4;
	let number5 = 5;

	number1 *= number1;
	number2 *= number2;
	number3 *= number3;
	number4 *= number4;
	number5 *= number5;

	console.log(`1 squared is ${number1}, 2 squared is ${number2}, 3 squared is ${number3}, 4 squared is ${number4}, 5 squared is ${number5}`);
}


squareNumbers()